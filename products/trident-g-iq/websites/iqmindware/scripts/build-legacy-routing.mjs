import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  KNOWN_JUNK_QUERY_PARAMS,
  canonicalTargetPathExists,
  createRuleIndex,
  detectRedirectProblems,
  expandExactSources,
  loadCanonicalInventory,
  loadJson,
  loadLegacyMap,
  pathHandledByFunctions,
  writeJson,
  writeText,
} from "./lib/legacy-routing-core.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const SITE_ROOT = path.resolve(__dirname, "..");

const MAP_PATH = path.join(SITE_ROOT, "seo", "legacy-url-map.csv");
const ROUTES_PATH = path.join(SITE_ROOT, "functions", "_routes.json");
const ROOT_REDIRECTS_PATH = path.join(SITE_ROOT, "_redirects");
const PUBLIC_REDIRECTS_PATH = path.join(SITE_ROOT, "public", "_redirects");
const LOOKUP_OUTPUT_PATH = path.join(
  SITE_ROOT,
  "functions",
  "_generated",
  "legacy-routing.json"
);

function createPrefixRedirectLine(oldPath, targetPath) {
  const source = `${oldPath}*`;
  const destination =
    targetPath === "/" ? "/:splat" : `${targetPath}:splat`.replace("//", "/");
  return `${source} ${destination} 301`;
}

function ruleForJson(rule) {
  return {
    match_type: rule.matchType,
    old_path: rule.oldPath,
    action: rule.action,
    target_path: rule.targetPath,
    strip_params: rule.stripParams,
    notes: rule.notes,
    source_line: rule.sourceLine,
  };
}

const args = new Set(process.argv.slice(2));
const mirrorPublicRedirects = args.has("--mirror-public");

const mapRows = await loadLegacyMap(MAP_PATH);
const routesConfig = await loadJson(ROUTES_PATH);
const canonicalInventory = await loadCanonicalInventory(SITE_ROOT);
const allRuleIndex = createRuleIndex(mapRows);

const errors = [];

for (const row of mapRows) {
  if (row.action === "301" && !canonicalTargetPathExists(row.targetPath, canonicalInventory)) {
    errors.push(
      `301 target does not exist in current inventory: ${row.oldPath} -> ${row.targetPath}`
    );
  }
}

errors.push(...detectRedirectProblems(mapRows, allRuleIndex));

const staticRedirectLines = new Set();
const functionRules = [];

for (const row of mapRows) {
  const handledByFunction = pathHandledByFunctions(row.oldPath, routesConfig);
  if (row.action !== "301") {
    if (!handledByFunction) {
      errors.push(
        `Rule ${row.oldPath} (${row.action}) is not on a Function route and cannot be applied by _redirects.`
      );
    }
    functionRules.push(row);
    continue;
  }

  if (handledByFunction) {
    functionRules.push(row);
    continue;
  }

  if (row.matchType === "exact") {
    for (const source of expandExactSources(row.oldPath)) {
      if (pathHandledByFunctions(source, routesConfig)) {
        errors.push(
          `No-overlap violation: ${source} is handled by Functions but would be emitted to _redirects.`
        );
        continue;
      }
      staticRedirectLines.add(`${source} ${row.targetPath} 301`);
    }
    continue;
  }

  if (row.matchType === "prefix") {
    if (pathHandledByFunctions(row.oldPath, routesConfig)) {
      errors.push(
        `No-overlap violation: prefix ${row.oldPath} is handled by Functions but would be emitted to _redirects.`
      );
      continue;
    }
    staticRedirectLines.add(createPrefixRedirectLine(row.oldPath, row.targetPath));
  }
}

if (errors.length > 0) {
  const details = errors.map((message) => `- ${message}`).join("\n");
  throw new Error(`Legacy routing build failed:\n${details}`);
}

const sortedRedirectLines = [...staticRedirectLines].sort((left, right) =>
  left.localeCompare(right)
);
const redirectsPayload = [
  "# Auto-generated by scripts/build-legacy-routing.mjs",
  "# Root _redirects is the only deploy-authoritative redirect artifact.",
  ...sortedRedirectLines,
].join("\n");

await writeText(ROOT_REDIRECTS_PATH, `${redirectsPayload}\n`);
if (mirrorPublicRedirects) {
  await writeText(PUBLIC_REDIRECTS_PATH, `${redirectsPayload}\n`);
}

const functionIndex = createRuleIndex(functionRules);
const generatedLookup = {
  generated_at: new Date().toISOString(),
  canonical_host: "https://www.iqmindware.com",
  mirror_public_redirects: mirrorPublicRedirects,
  known_strip_allowlist: [...KNOWN_JUNK_QUERY_PARAMS].sort(),
  rules: functionRules.map(ruleForJson),
  exact: Object.fromEntries(
    [...functionIndex.exact.entries()].map(([key, row]) => [key, ruleForJson(row)])
  ),
  prefix: functionIndex.prefix.map(ruleForJson),
};

await writeJson(LOOKUP_OUTPUT_PATH, generatedLookup);

console.log(`Generated root _redirects: ${ROOT_REDIRECTS_PATH}`);
console.log(`Generated function lookup: ${LOOKUP_OUTPUT_PATH}`);
if (mirrorPublicRedirects) {
  console.log(`Generated optional mirror: ${PUBLIC_REDIRECTS_PATH}`);
} else {
  console.log("Skipped public/_redirects mirror (use --mirror-public to generate).");
}
